    public String getID(String username) {
        String memberid = null;

        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            //get member ID from username
            conn = DBConnect.getConnection();
            ps = conn.prepareStatement("SELECT `login`.`memberID` FROM `techdatingdb`.`login` WHERE `login`.`username` ='" + username + "';");

            rs = ps.executeQuery();

            while (rs.next()) {
                SharedData.memberID = rs.getString(1);
                rs.close();
            }
        } catch (ClassNotFoundException | SQLException e) {
            DBConnect.close(conn, ps, rs);
            e.printStackTrace();
        }

        memberid = SharedData.memberID;
        return memberid;
    }


