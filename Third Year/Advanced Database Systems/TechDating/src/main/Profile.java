    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        String username = SharedData.getUsername();
        String memberid = getID(username);

        int id = Integer.parseInt(memberid);
        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {

            //establish connection
            conn = DBConnect.getConnection();

            //query to call the get profile procedure
            ps = conn.prepareStatement("CALL get_profile("+ id+ ");");
            rs = ps.executeQuery();

            //create table model
            DefaultTableModel tableModel = new DefaultTableModel();
            //get meta data
            ResultSetMetaData metaData = rs.getMetaData();
            //get column names
            int columnCount = metaData.getColumnCount();
            for (int i = 1; i <= columnCount; i++) {
                tableModel.addColumn(metaData.getColumnLabel(i));
            }

            Object[] row = new Object[columnCount];

            while (rs.next()) {
                for (int i = 0; i < columnCount; i++) {
                    row[i] = rs.getObject(i + 1);
                }
                tableModel.addRow(row);
            }

            //apply table model to tblProfile
            tblProfile.setModel(tableModel);

            //close all connections
            rs.close();
            ps.close();
            conn.close();

        } catch (ClassNotFoundException | SQLException e) {
            DBConnect.close(conn, ps, rs);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnRefreshActionPerformed

